<p>
Amsterdam is the capital of the Netherlands, and it is one of Europe's most picturesque capitals. Amsterdammers are proud of their world-renowned 17th-century canal system, which was awarded UNESCO world heritage status in 2010. The city also boasts 75 museums, among which the Rijksmuseum, the Van Gogh Museum and modern art at the Stedelijk, all conveniently located in the museum district, a five minute tram-ride from the venue. Their collections including key works by the likes of Van Gogh, Rembrandt and Mondriaan.
Further must-sees on any visitor's itinerary include the Anne Frank House and the world's only floating flower market. With over 400 km of cycle paths, cycling is the most convenient way to enjoy Amsterdam’s sites and atmosphere (there are in fact more bicycles than people in Amsterdam). Rent a bike and join thousands of locals navigating Amsterdam's labyrinthine streets, or just take in the sights on foot.
</p><p>
Amsterdam is very internationally oriented with a rich history in international trade relations and good airline connections to most major international airports. Almost everyone speaks English, from the tram driver to the lady who sells flowers at a small street stand. If that’s not enough, there are over 1500 bars and cafés in Amsterdam.
</p>

