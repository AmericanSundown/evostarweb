<h3>Lisbon-Porto BY TRAIN</h3>
<p>
Rail travel from Lisbon to Porto is the suggested travel mode as the prices of buses and 

hire car/road tolls is about the same as the train, and the journey is scenic and relaxed.  

Two types of train are available:  the Intercity (Intercidades) and the Alfa Pendular, which 

is 30 minutes faster with more comfortable seats and free WIFI.  Trains leave regularly 

throughout the day and Information on timetables and prices are at <a href="http://www.cp.pt">www.CP.pt</a>
</p>

<p>
Current indicative prices for first and second class tickets in each direction are : Alfa 

Pendular is €42.40/€30.30 and the Intercidades costs €35.90/€24.30 
</p>
<p>

Both types of train leave from the Lisboa Santa Apolonia and Oriente stations. 
</p>
<p>

The arrival station in Porto is at Campanhã station, to the east of the centre, but you can 

either take the metro or with your Lisboa-Porto ticket, you are allowed to transfer to a 

suburban train free of charge for the five minute journey to São Bento station in the city 

centre. Taxis are also available as well as local buses.
</p>

<h3>BY CAR</h3>

Porto is literally surrounded by motorways, meaning the city enjoys excellent road connections with 

the entire Portugal. Thus, for instance, A1 links Porto to Lisbon, A4 provides the connection to 

Trás-os-Montes, whereas A3 reaches all the way to Minho. Braga, Aveiro, Viana do Castelo are 

also connected by motorways to Porto, so, depending on the chosen destination, you must opt for 

either A29 or A28 in order to get to where they want to reach. Either by bus or by car (private 

vehicle or rented car), Porto can be reached from virtually all corners of the country. Estradas de 

Portugal offers complete information on the motorway and national road networks of Portugal, 

which is why foreigners who want to get to Porto are referred to their <a href="http://www.estradasdeportugal.pt/">official website.</a>

<h3>BY BUS</h3>
<p>
Getting to Porto by bus is yet another inspired alternative for people who travel from abroad and 

for nationals alike. Bus trips have their inner advantages, and they can be taken into account by 

people who, for one reason or another, rule out flights and train trips, eventually opting for this 

manner of transport.</p>
<p>
The Lisbon headquartered Rede Nacional de Expressos links Porto to virtually the entire Portugal. 

One of the most frequented routes is the one between Porto and Lisbon. Thus, the company 

operates 9 trips a day between the capital and Porto, which is fairly reassuring for all tourists 

(business and pleasure tourists alike) who need to get from one destination to another in a 

relatively short time.
</p>

For complete and updated information on the bus trips, arrivals and departures, network of lines, 

as well as ticket prices and the like, see <a href="http://www.rede-expressos.pt/default.aspx">Rede Nacional de Expressos</a>.
