   <h3>How To Get To Copenhagen:</h3>

<h4>By Plane:</h4>

Copenhagen's airport has direct flights from many cities within Europe
and from some within the United States
(<a href="http://www.cph.dk/en/flight-info/route-map/">http://www.cph.dk/en/flight-info/route-map/</a>). The airport is located
on the island of Amager, which is a 20 minute Metro ride away from the
centre.

<h4>By Train:</h4>

Copenhagen can also conveniently be reached by direct internal trains
from Hamburg, Stockholm, Basel, and Amsterdam, among other
(<a href="http://www.dsb.dk/om-dsb/in-english/">http://www.dsb.dk/om-dsb/in-english/</a>). The Copenhagen Central Station
is around a 10 minute walk away from the conference venue.

<h3>From Copenhagen Airport to the Centre:</h3>

