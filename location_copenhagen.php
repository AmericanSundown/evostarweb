<h3> Bicycle</h3>



Bicycles are by far the most popular and convenient mean of transportation in 

Copenhagen. There are more than 300km of bicycle lanes and many guided 

bicycle tours.

Bikes can be rented in almost all bike shops; moreover, a public bike sharing 

service called City Bike is also active in the city

<h4>Copenhagen City Bike</h4>

The first time you use the city bike you rent the bike by entering your credit card 

information and unlock the city bike on the small tablet which is located on the 

handlebars. The bikes can be returned to any station in the system.

You can register yourself as a user online before your rental and save some time.

<h3>Public Transportation</h3>

The public transportation in Copenhagen is very reliable, punctual, and it takes 

you everywhere. In Copenhagen the trains, Metro and busses (including 

waterbuses) can be accessed with the same ticket. All you need to know is how 

many zones you will pass on your journey. Note the Metro will take you from the 

airport to central Copenhagen in only 20 minutes.

<h4>Tickets</h4>

You can buy different kinds of tickets when using public transportation in 

Copenhagen. It all depends on how long you are staying in the city, and how 

much you are planning on using public transportation.

Always buy a ticket before boarding the train or metro. On-board the buses, you 

also have the possibility to buy your ticket from the driver, if you bring small 

change. The same ticket can be used in the metro, bus, train and harbour bus. 

Always have your ticket with you in case a conductor wants to see it. Travelling 

without a valid ticket will result in a fine.

<p>

The most convenient options you have for the tickets are:
</p>

<ul>
<li><strong>72-hour City Pass in zones 1-4</strong>

The City Pass gives you unlimited access to buses, trains, metro and 

harbour buses in zones 1, 2, 3 and 4, which includes the centre of 

Copenhagen and to and from the airport.

A adult 72-hour City Pass costs DKK 200. Buy this ticket at the airport, 

station or buy it here.</li>

<li><strong>Flexcard - 7 days </strong>

You can also purchase a 7-day unlimited travel card called a Flexcard. It 

gives you unlimited travel in your choice of zones (1-2). The price is DKK 

250. The card is available for longer periods of time as well and is sold in 

ticket sales at the stations (also at the airport).</li>

<li><strong>72-hour Copenhagen Card</strong>

A Copenhagen Card gives you unlimited travel on buses, trains, metro and 

harbour buses in all zones in the capital region. In addition, you get free 

entrance to 72 major attractions and museums as well as discounts in 

several restaurants, sights, car rental and more.

An adult 72-hours Copenhagen Card costs approximately DKK 580</li>
</ul>


<h4>Buses</h4>

The A-buses are the primary buses in central Copenhagen. They drive every 3-7 

minutes during rush hour (which is between 07:00-09:00 in the morning and 

15:30-17:30 in the afternoon) and usually about every 10 minutes before and 

after rush hour. The A-buses serve at all hours.

The S-buses drive every 5-10 minutes during rush hour and every 20 minutes 

outside of rush hour. The S-buses are faster than the A-busses due to fewer 

stops. They usually serve between 06:00 in the morning and 01:00 at night.

Night buses are in service between 01:00 and 05:00 in the night. You can 

recognise the bus stops by the grey colour of the bus stop signs. The night buses 

are all N buses, for an example 85N.

<h4>Metro</h4>

The metro is in service all day and all night, every day of the week.

There are 2-4 minutes between each train during rush hour, and 3-6 minutes 

outside rush hour and during the weekends. Friday and Saturday night (after 

01:00) trains arrive with a 7-15 minutes interval and a 20 minutes interval after 

midnight on weekdays (Sunday through Thursday).

Please note that maintenance of the rails may occur during the night, which can 

result in a slightly longer journey. The maintenance is between 01:00-01:25 and 

again between 04:15-04:45.

<h4>Trains</h4>

The S-trains run between 05:00 in the morning and 00:30 at night. Line F runs 

every 4-5 minutes, line A, B, C and E run every 10 minutes, and line H and Bx run 

every 20 minutes.

On Friday and Saturday the trains run once an hour between 01:00 and 05:00. 

However, line F runs every half hour during these hours.

