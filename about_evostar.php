<li>EuroGP 18th European Conference on Genetic Programming</li>

<li>EvoBIO  13th European Conference on Evolutionary Computation, Machine Learning and Data Mining in Computational Biology</li>

<li>EvoCOP 15th European Conference on Evolutionary Computation in Combinatorial Optimisation</li>

<li>EvoMUSART 4rd International Conference (and 12th European event) on Evolutionary and Biologically Inspired Music, Sound, Art and Design</li>

<li>EvoApplications  17th European Conference on the Applications of Evolutionary and bio-inspired Computation including the following tracks</li>
