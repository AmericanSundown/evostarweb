<p>
Evo<span class="red">Star</span> 2015 will be held at the <stron>National Museum of Denmark</strong> in the centre of 
Copenhagen which is housed in an 18th century palace. 
The museum  is  located in  the city    center  between  Rådhusplads (the    city    hall
square) and Christiansborg  (the    Danish  parliament).</p>

<p>
Opening at 9am on Wednesday, 8 April with invited speaker <a href="speaker_oudeyer.php">Pierre-Yves 
Oudeyer</a>, EvoStar runs until 
lunchtime on Friday, 10 April following the closing talk by invited speaker  

 <a href="programme_speakers.php">Paulien Hogeweg</a>,  The programme 
will be posted at the website by early February.
</p>

<p>
A local social trip will take place on Friday afternoon with details to be

announced shortly.
</p>

<p>
The National Museum of Denmark offers a wealth of historical exhibits and these 
are open to all EvoStar participants.
Good AV facilities are available including WIFI but authors should bring their own 
laptops/tablets to present their work, or make arrangements with colleagues if 
bringing presentations on a USB stick.
</p>




