<h2>
	Non-random random mutations: Evolution of Genotype-Phenoptype mapping, by <span class="red">Paulien Hogeweg</span>
</h2>

<p>
	<strong>Abstract: </strong> 


Darwinian evolution, i.e. random mutations and selection, not only leads to
adaptation to the environment, but more importantly, to the 
complex multilayered genotype-phenotype mapping, and therewith the mutational neighbourhood.

In evolutionary models with enough degrees of freedom, information integration over evolutionary time will shape the mutational neighbourhood sothat (the effect of) random mutations is not random but biased to be
 beneficial.

I will show that the structuring of the mutational neighbourbood,
 may serve as an alternative to  more well known processes like regulation and speciation to cope with temporal and spatial heterogeneous environments. 
</p>

