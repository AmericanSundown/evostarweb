<p><strong>Application of Nature-inspired Techniques for Communication Networks and other Parallel and Distributed Systems</strong></p>

Evo<span class="red">COMNET</span> solicits contributions dealing with the application of ideas from natural processes and systems to the definition, analysis, and development of novel sequential, parallel, and distributed evolutionary computation approaches to the solution of problems of practical and theoretical interest in all domains related to network systems. Use and tuning of hybrid computational approaches based on evolutionary techniques are also welcome.
Topics of interest include, but are not limited to:
<ul>
<li><em>Communication systems</em>: Telecommunication, mobile, satellite, voice, and optical communications, Distributed inference and cooperative communication systems, Network analysis and design, Switching and Routing</li>
<li><em>Networks: Sensor networks</em>, Mobile ad hoc networks, Peer-to-peer networks, Distributed search and computing in peer-to-peer networks, Network protection systems, Network robotics and sensor-actor networks</li>
<li><em>Network-related issues</em>: Parallel and distributed optimization algorithms, Grid and cloud computing, Load balancing, routing, scheduling, and mapping in network-based computing systems, Quality-of-service provisioning, Distributed data mining</li>
<li><em>Abstract network systems</em>: Transportation, Supply, Logistics, Traffic management and simulation, Social network systems, Financial and economic network systems</li>
<li><em>Real-world implementations</em></li>
</ul>

